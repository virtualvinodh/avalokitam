'use strict'
// Metre-detection integration tests using the example verses from /types page.
// Each verse must be identified as the exact metre string the parser returns.
// Run with: node --test test/types.test.js

const { describe, it } = require('node:test')
const assert = require('node:assert/strict')
require('dotenv').config()
const { callParser, parseXML } = require('../geminiLoop')

async function getMetre (verse) {
  const xml = await callParser(verse.trim())
  const result = await parseXML(xml)
  return result.metreDetected || ''
}

async function assertMetre (verse, expected) {
  const metre = await getMetre(verse)
  assert.equal(metre, expected)
}

