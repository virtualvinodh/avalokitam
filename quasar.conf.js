        const sassOverrides = {
          implementation: sass,
          sassOptions: {
            functions: {
              'quote($val)': function (val) {
                const str = val instanceof sass.types.Number
                  ? val.getValue().toString()
                  : val instanceof sass.types.String ? val.getValue() : String(val)
                return new sass.types.String('"' + str + '"')
              }
            }
          }
        }
